// math.min is used for solving the lowest number or value
const result = Math.min(1, 3, 5, 12, 3, 6, 1, 4, 4);
console.log(result);

//math.max is used for dolving the highest number or value
const result2 = Math.max(1, 32, 5, 8, 3, 1, 7, 8);
console.log(result2);

//Math.pi is is the value of PI
console.log(Math.PI);

//Math.abs works as modulas
